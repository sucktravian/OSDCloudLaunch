Function Get-Proxy {

    Get-ItemProperty -Path 'HKCU:\Software\Microsoft\Windows\CurrentVersion\Internet Settings' | Select-Object ProxyServer, @{N = "ProxyEnable"; E = { switch ($_.ProxyEnable) {
                "0" { "無効" }
                "1" { "有効" }
            } }
    }
}
